# Faca um programa que receba dois numeros e ao final mostre a soma, subtracao, multiplicacao e a divisao dos numeros lidos.

n1 = float(input("informe o primeiro numero:"))
n2 = float(input("informe o segundo numero:"))

soma = n1 + n2
sub = n1 - n2
multi = n1 * n2
div = n1 / n2

print ("soma:", soma, "subtracao:",sub, "multiplicacao:", multi, "divisao:", div)

