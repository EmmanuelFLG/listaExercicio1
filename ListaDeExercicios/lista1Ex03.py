# Ler uma temperatura em graus Celsius e apresenta-la convertida em graus Fahrenheit. A formula da conversao e: F=(9*C+160)/5, 
# sendo F a temperatura em Fahrenheit e C a temperatura em Celsius.

c = int(input("informe o valor da temperatura em graus Celsius:"))

f = (9 * c + 160)/5

print ("A conversao de", c, "° Celsius para Fahrenheit:", f, "° Fahrenheit")

